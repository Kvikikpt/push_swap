#include "./libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	a;
	char	*str;

	a = 0;
	str = (char *)s;
	while (a < n)
	{
		str[a] = c;
		a++;
	}
	return (s);
}
